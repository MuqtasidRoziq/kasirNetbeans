    private void loadDataProduk() {
        DefaultTableModel model = (DefaultTableModel) tabProduk.getModel();
        model.setRowCount(0); // Reset tabel

        try {
            Connection con = koneksi.getConnection();
            Statement st = con.createStatement();
            String query = "SELECT t.tanggal_transaksi AS tanggal, p.nama_produk, dt.jumlah AS jumlah "
                    + "FROM detail_transaksi dt "
                    + "JOIN produk p ON dt.id_produk = p.id_produk "
                    + "JOIN transaksi t ON dt.id_transaksi = t.id_transaksi "
                    + "ORDER BY t.tanggal_transaksi DESC"; 
            ResultSet rs = st.executeQuery(query);
            
            SimpleDateFormat dbFormat = new SimpleDateFormat("yyyy-MM-dd"); 
            SimpleDateFormat ubahFormat = new SimpleDateFormat("dd-MMMM-yyyy");

            while (rs.next()) {
                String tanggal = rs.getString("tanggal");
                String namaProduk = rs.getString("nama_produk");
                String totalJual = rs.getString("jumlah");
                
                try {
                    tanggal = ubahFormat.format(dbFormat.parse(tanggal));
                } catch (Exception e) {
                    tanggal = "Invalid Date";
                }
                
                // Tambahkan data ke model tabel
                model.addRow(new Object[]{tanggal, namaProduk, totalJual});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    } 
